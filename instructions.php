<p><strong>Step 1 (For all the Applicants)</strong></p>

<p><strong>Please read the following instructions carefully before filling the online admission application form.</strong></p>

<ol>
  <li>Download prospectus from&nbsp;<a href="https://www.ue.edu.pk/admissions/2023"><strong>https://www.ue.edu.pk/admissions/2023</strong></a></li>
  <li><strong>Please check your eligibility from the following URL</strong>&nbsp;<a href="https://www.ue.edu.pk/admissions/2023"><strong>https://www.ue.edu.pk/admissions/2023</strong></a>&nbsp;<strong>before applying for admission.</strong></li>
  <li>Following documents will be required for completion of online admission form.
  <ol>
    <li><strong>CNIC/B-Form</strong>&nbsp;of Candidate</li>
    <li><strong>CNIC</strong>&nbsp;of Father/Guardian</li>
    <li><strong>Certificates</strong>&nbsp;of Secondary School and Higher Secondary School</li>
    <li><strong>Transcripts</strong>&nbsp;of ADP/Bachelor and Master programs (if applying for&nbsp;<strong>MS/MPhil/MBA program</strong>)</li>
    <li><strong>Transcripts</strong>&nbsp;of ADP/Bachelor, Master and MS/MPhil programs (if applying for&nbsp;<strong>PhD program</strong>)</li>
    <li>Scanned photo in&nbsp;<strong>JPEG</strong>&nbsp;format upto&nbsp;<strong>50KB</strong>&nbsp;size.</li>
  </ol>
  </li>
  <li>Please note that after pressing the &ldquo;<strong>Submit</strong>&rdquo; button, applicant will not be able to edit the followings; however, the rest of the fields available&nbsp;may be edited.
  <ol>
    <li>Campus/Division</li>
    <li>Degree Level</li>
    <li>Subject</li>
    <li>Shift</li>
    <li>Applicant&#39;s Name</li>
    <li>Applicant&#39;s CNIC</li>
    <li>Father&#39;s Name</li>
  </ol>
  </li>
  <li>Print the&nbsp;<strong>Fee Challan,&nbsp;</strong>deposit the prescribed admission processing fee in any branch of The Bank of Punjab (BOP) and get computer generated deposit receipt (Customer Copy).</li>
  <li><strong>Please note that printed application form / computer generated deposit receipt (Customer copy) need&nbsp;not to be submitted to the university but should be kept for record.&nbsp;</strong></li>
  <li>Only those applicants will be considered for admission who have deposited the prescribed admission processing fee.</li>
  <li><strong>Submit separate online application form for each campus/ program/ shift/ quota.</strong></li>
  <li><strong>Separate admission processing fee will be charged for each application form.</strong></li>
  <li>The applicants having unusual or non-standard qualifications (e.g. diploma, certificate, etc.) are required to seek equivalence (prior to applying online for admission) from Quality Enhancement Cell, University of Education, Lahore. They will have to submit equivalence form (which may be downloaded from UE Website&nbsp;<strong>(i.e.&nbsp;</strong><a href="https://www.ue.edu.pk/admissions/2023"><strong>www.ue.edu.pk</strong></a><strong>)</strong>) in Quality Enhancement Cell, University of Education, College Road, Township, Lahore and deposit prescribed fee. For any query regarding equivalence, please contact Quality Enhancement Cell at&nbsp;<strong>directorqec@ue.edu.pk</strong>.&nbsp;</li>
  <li>Merit Lists and Waiting List will be available on the following URL&nbsp;<a href="https://www.ue.edu.pk/admissions/2023"><strong>https://www.ue.edu.pk/admissions/2023</strong></a>&nbsp;as per scheduled dates mentioned in UE Admission Calendar available in UE Prospectus as well as uploaded on UE website&nbsp;<strong>(</strong><a href="http://www.ue.edu.pk/admissions/2023" target="_blank"><strong>www.ue.edu.pk</strong></a><strong>)</strong></li>
  <li>For any kind of information, complaint and queries, regarding online admission form, please contact the concerned Division/Campus on the phone number given in the Advertisement/University Website/Prospectus. You may also contact on the following email address.</li>
</ol>

<p><strong>Email Address:&nbsp;admissions@ue.edu.pk</strong></p>

<p>&nbsp; &nbsp; &nbsp; &nbsp;&nbsp;</p>

<p><strong>Step 2&nbsp;(For Successful Applicants i.e. applicants appearing in Merit List)</strong></p>

<ol>
  <li>The applicants, whose names are displayed in the merit lists, will contact the Admission Incharge of the relevant Division/Campus, along-with original educational documents, CNIC/Form-B and Father/Guardian CNIC, two sets of photocopies of all these documents.</li>
  <li>After completion of verification process, the Admission Office of concerned Division/Campus will issue a fee Challan for deposit of&nbsp;<strong>Admission dues (Semester fee)</strong>.</li>
  <li>The applicant will deposit the fee in any Branch of The Bank of Punjab (BOP) within due dates and get the computer generated deposit receipt (Customer Copy); however, he/she needs not to submit the computer generated deposit receipt (Customer Copy) in the Division/Campus concerned but should be kept for record.</li>
  <li>If an applicant does not submit the&nbsp;<strong>Admission dues</strong>&nbsp;<strong>(Semester dues)</strong>&nbsp;within due date, the admission offer shall stand cancelled.</li>
</ol>

<p><strong>Additional Instructions for Applicants of MS/MPhil/MBA and PhD Programs</strong></p>

<ol>
  <li>The applicant has to select test center&nbsp;<strong>where he/she is interested to apply for the admission</strong>&nbsp;of&nbsp;<strong>MS/MPhil and PhD</strong>&nbsp;program.</li>
  <li>The&nbsp;<strong>applicants of MS/MPhil/MBA/PhD</strong>&nbsp;will have to appear in&nbsp;<strong>UE Entry Test&nbsp;(GAT + Subject based)</strong>&nbsp;at concerned UE Division/Campus (where applied for admission) on&nbsp;<strong>January 10, 2023 at 10:00 a.m.&nbsp;(Reporting time will be 09:00 a.m. at their concerned UE Division/Campus).</strong></li>
  <li>The applicants of PhD programs are required to take UE Entry Test (Subject Based Test) administered by UE Entry Test Committee. Moreover, the applicants holding valid Graduate Record Examination (GRE) administered by Education Testing Service (ETS)/Education Testing Council (ETC) will be exempted from the UE Entry Test.</li>
  <li>List of successful applicants of <strong>MS/MPhil/MBA/PhD</strong> will be displayed on the University Website&nbsp;<strong>(i.e.&nbsp;</strong><a href="https://www.ue.edu.pk/admissions/2023"><strong>www.ue.edu.pk</strong></a><strong>)&nbsp;on</strong>&nbsp;<strong>January 11, 2023.</strong></li>
  <li>The successful applicants of&nbsp;<strong>MS/MPhil/MBA/PhD programs&nbsp;</strong>will appear for the interview at concerned UE Division/Campus (where applied for admission) on&nbsp;<strong>January 12, 2023 at 10:00 a.m.&nbsp;(Reporting time will be 09:00 a.m. at their concerned UE Division/Campus).</strong></li>
  <li>After interview,&nbsp;<strong>1<sup>st</sup> merit list&nbsp;of MS/MPhil/MBA/PhD</strong> will be displayed on University Website on&nbsp;<strong>January 13, 2023</strong>&nbsp;and applicants will have to go through the process given in the Step 2 (For Successful Applicants i.e. applicants appearing in Merit List).</li>
</ol>

<p>Note:</p>

<ol>
  <li>Any forgery in transcripts or tampering in marks will result in cancellation of application at any stage even after the admission and this will make the applicant ineligible for life time for admission at University of Education, Lahore.</li>
</ol>

